function CoinConstructor(
  id,
  name,
  symbol,
) {
  this.id = id,
  this.name = name,
  this.symbol = symbol;
}

function MoreInfoConstructor(image, ils_Price, usd_Price, eur_Price) {
  this.image = image,
  this.ils_Price = ils_Price,
  this.usd_Price = usd_Price,
  this.eur_Price = eur_Price;
}
